    private void update_item_price(){
        double item_price = 0;
        
        if(single_btn.isSelected())
            item_price = 3.50;
        else if(double_btn.isSelected())
            item_price = 4.75;
        else{
            JOptionPane.showMessageDialog(this, "Burger type must be selected.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Add charges
        if(cheese_check.isSelected())
            item_price += 0.50;
        
        if(bacon_check.isSelected())
            item_price += 0.25;
        
        if(meal_check.isSelected())
            item_price += 4.00;
        
        DecimalFormat fmt = new DecimalFormat("#,##0.00");
        price_box.setText(fmt.format(item_price));
    }
    
    private void clear_for_next_item(){
        btn_group.clearSelection();
        cheese_check.setSelected(false);
        bacon_check.setSelected(false);
        meal_check.setSelected(false);
        price_box.setText("0");
        qty_box.setText("1");
    }
