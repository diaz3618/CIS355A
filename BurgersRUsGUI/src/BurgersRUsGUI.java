    private void mnu_add_toActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnu_add_toActionPerformed
        // Output line
        String output_line = qty_box.getText();
        if(single_btn.isSelected())
            output_line += " single";
        else
            output_line += " double";
        
        if(cheese_check.isSelected())
            output_line += ", cheese";
        if(bacon_check.isSelected())
            output_line += ", bacon";
        if(meal_check.isSelected())
            output_line += ", meal";
        
        output_line += " at ";
        output_line += price_box.getText();
        output_line += " each\n";
        
        order_box.append(output_line);
        
        // Update total
        int qty = Integer.parseInt(qty_box.getText());
        double item_price = Double.parseDouble(price_box.getText());
        double line_price = qty * item_price;
        
        order_total += line_price;
        DecimalFormat fmt = new DecimalFormat("#,##0.00");
        total_box.setText(fmt.format(order_total));
    }//GEN-LAST:event_mnu_add_toActionPerformed

